package week7_Midweek;

public class Task3_Midweek {
    /*
        3. Write an infinite loop. An infinite loop never ends. Condition is always true.
 */

    //Main method
    public static void main(String[] args) {
        for (int i=0; i<1; i--) { //loop never ends as the condition is always true
            System.out.println("Hello JAVA!"); //print statement in the loop
        }
    }
}
